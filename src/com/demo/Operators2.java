package com.demo;

public class Operators2 
{
public static void main(String[] args) 
{
			//Now we can Learn About the Increment and Decrement 
			// variables 
			int a = 9 ;
			if (a==10)
			{
				System.out.println("Increment BY 10..." + (++a));
				System.out.println("Increment BY 10..."+ (a++));
				System.out.println("Increment BY 10..."+a);
			}
			else
			{
				System.out.println("Decrement Values..."+ (--a));
				System.out.println("Decrement Values..."+ (a--));
				System.out.println("Decrement Values..."+ (a));
				
			}
			
				System.out.println("Today We Make a Operators Logic So Now let's Get Start...");
			
}
}
